"""Фикс наведения на оружие: mouseOverText = stats.name + ", " + ammo + " rounds" (декомпил 15482).
Старый generic-regex r:"^(.+?),\s+(\d+)\s+rounds$"=$1,$2 прогонял ИМЯ оружия насквозь по-английски
(XUnity не переводит захваченную группу). Заменяем на per-weapon regex по реальному набору из 37
WeaponPreset.name (tools/dump_weapon_names.py), переиспользуя уже существующий перевод имени.
Плюс чистим авто-кэш XUnity от плохих закэшированных строк (иначе точный кэш перекрыл бы regex)."""
import io, os, re

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
TEXTDIR = os.path.join(ROOT, "BepInEx", "Translation", "ru", "Text")
FIXES = os.path.join(TEXTDIR, "ui_fixes.txt")
CACHE = os.path.join(TEXTDIR, "_AutoGeneratedTranslations.txt")

# 37 отображаемых имён (WeaponPreset.name) -> русский (значения = уже существующие плейн-ключи).
# PDW→ПОС (персональное оружие самообороны), Stench Gun→Газомёт — по запросу. Vitya/PDW — добор.
